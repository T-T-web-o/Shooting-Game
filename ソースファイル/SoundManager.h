#pragma once
//============================================================
// SoundManagerクラス
// 効果音の読み込み・管理を行うクラス
// すべて static メンバとして管理し、
// どのシーンからでも利用できるようにしている
//============================================================
class SoundManager
{
public:
	//------------------------------------------------------------
	// サウンド読み込み
	//------------------------------------------------------------
	static void Load();

	//------------------------------------------------------------
	// サウンド解放
	//------------------------------------------------------------
	static void Release();

	//------------------------------------------------------------
	// 効果音ハンドル
	//------------------------------------------------------------
	static int titleSE;  // タイトル決定音
	static int shotSE;   // ショット音
	static int hitSE;    // ヒット音
	
};

